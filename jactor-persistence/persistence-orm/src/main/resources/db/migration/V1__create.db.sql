CREATE SEQUENCE PER_SEQU;
CREATE TABLE T_PERSON (
  ID            BIGINT GENERATED BY DEFAULT AS SEQUENCE PER_SEQU PRIMARY KEY,
  CREATION_TIME VARCHAR(50),
  CREATED_BY    VARCHAR(50),
  UPDATED_TIME  VARCHAR(50),
  UPDATED_BY    VARCHAR(50),
  DESCRIPTION   VARCHAR(250),
  FIRST_NAME    VARCHAR(50),
  SURNAME       VARCHAR(50) NOT NULL,
  LOCALE        VARCHAR(10),
  USER_ID       BIGINT,
  ADDRESS_ID    BIGINT
);

CREATE SEQUENCE US_SEQU;
CREATE TABLE T_USER (
  ID            BIGINT GENERATED BY DEFAULT AS SEQUENCE US_SEQU PRIMARY KEY,
  CREATION_TIME VARCHAR(50),
  CREATED_BY    VARCHAR(50),
  UPDATED_TIME  VARCHAR(50),
  UPDATED_BY    VARCHAR(50),
  EMAIL         VARCHAR(50),
  USER_NAME     VARCHAR(50) NOT NULL,
  PERSON_ID     BIGINT      NOT NULL
);
