CREATE SEQUENCE ADD_SEQU;
CREATE TABLE T_ADDRESS (
  ID             BIGINT GENERATED BY DEFAULT AS SEQUENCE ADD_SEQU PRIMARY KEY,
  CREATION_TIME  VARCHAR(50),
  CREATED_BY     VARCHAR(50),
  UPDATED_TIME   VARCHAR(50),
  UPDATED_BY     VARCHAR(50),
  ADDRESS_LINE_1 VARCHAR(50) NOT NULL,
  ADDRESS_LINE_2 VARCHAR(50),
  ADDRESS_LINE_3 VARCHAR(50),
  CITY           VARCHAR(50) NOT NULL,
  COUNTRY        VARCHAR(7),
  ZIP_CODE       INTEGER     NOT NULL
);

CREATE SEQUENCE BL_SEQU;
CREATE TABLE T_BLOG (
  ID            BIGINT GENERATED BY DEFAULT AS SEQUENCE BL_SEQU PRIMARY KEY,
  CREATION_TIME VARCHAR(50),
  CREATED_BY    VARCHAR(50),
  UPDATED_TIME  VARCHAR(50),
  UPDATED_BY    VARCHAR(50),
  CREATED       VARCHAR(10) NOT NULL,
  TITLE         VARCHAR(50) NOT NULL,
  USER_ID       BIGINT      NOT NULL
);

CREATE SEQUENCE BLEN_SEQU;
CREATE TABLE T_BLOG_ENTRY (
  ID            BIGINT GENERATED BY DEFAULT AS SEQUENCE BLEN_SEQU PRIMARY KEY,
  CREATION_TIME VARCHAR(50),
  CREATED_BY    VARCHAR(50),
  UPDATED_TIME  VARCHAR(50),
  UPDATED_BY    VARCHAR(50),
  CREATED_TIME  VARCHAR(50)   NOT NULL,
  CREATOR_NAME  VARCHAR(50)   NOT NULL,
  ENTRY         VARCHAR(2500) NOT NULL,
  BLOG_ID       BIGINT        NOT NULL
);

CREATE SEQUENCE GB_SEQU;
CREATE TABLE T_GUEST_BOOK (
  ID            BIGINT GENERATED BY DEFAULT AS SEQUENCE GB_SEQU PRIMARY KEY,
  CREATION_TIME VARCHAR(50),
  CREATED_BY    VARCHAR(50),
  UPDATED_TIME  VARCHAR(50),
  UPDATED_BY    VARCHAR(50),
  TITLE         VARCHAR(50) NOT NULL,
  USER_ID       BIGINT      NOT NULL
);

CREATE SEQUENCE GBE_SEQU;
CREATE TABLE T_GUEST_BOOK_ENTRY (
  ID            BIGINT GENERATED BY DEFAULT AS SEQUENCE GBE_SEQU PRIMARY KEY,
  CREATION_TIME VARCHAR(50),
  CREATED_BY    VARCHAR(50),
  UPDATED_TIME  VARCHAR(50),
  UPDATED_BY    VARCHAR(50),
  CREATED_TIME  VARCHAR(50)   NOT NULL,
  GUEST_NAME    VARCHAR(50)   NOT NULL,
  ENTRY         VARCHAR(2500) NOT NULL,
